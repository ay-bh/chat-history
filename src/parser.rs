use regex::Regex;
use serde_json::Value;
use std::sync::LazyLock;

static CLEAN_USER_QUERY_RE: LazyLock<Regex> =
    LazyLock::new(|| Regex::new(r"<user_query>\s*").unwrap());
static CLEAN_END_QUERY_RE: LazyLock<Regex> =
    LazyLock::new(|| Regex::new(r"\s*</user_query>").unwrap());
static CLEAN_ATTACHED_RE: LazyLock<Regex> =
    LazyLock::new(|| Regex::new(r"(?s)<attached_files>.*?</attached_files>").unwrap());
static CLEAN_MULTI_NL_RE: LazyLock<Regex> = LazyLock::new(|| Regex::new(r"\n{3,}").unwrap());

static STRIP_TAGS: &[&str] = &[
    "open_and_recently_viewed_files",
    "user_info",
    "system_reminder",
    "git_status",
    "external_links",
    "rules",
    "agent_skills",
];

static STRIP_TAG_REGEXES: LazyLock<Vec<Regex>> = LazyLock::new(|| {
    STRIP_TAGS
        .iter()
        .map(|tag| Regex::new(&format!(r"(?s)<{tag}>.*?</{tag}>")).unwrap())
        .collect()
});

static NOISE_PATTERNS: &[&str] = &[
    "this session is being continued",
    "caveat:",
    "you are claude code",
    "read-only mode",
    "i cannot make changes",
    "i'm in plan mode",
    "hello! i'm claude",
    "i am claude",
    "ready to help you",
    "what would you like me to",
    "how can i assist",
    "i understand that i'm",
];

static WARMUP_PATTERNS: &[&str] = &["warmup", "/clear"];

static CLEAR_META_PATTERNS: &[&str] = &[
    "caveat: the messages below were generated by the user while running local commands",
    "<command-name>",
    "</command-name>",
    "<local-command-stdout>",
];

fn truncate_for_search(s: &str, max: usize) -> String {
    if s.len() <= max {
        return s.to_owned();
    }
    let head_end = s.floor_char_boundary(max * 3 / 4);
    let tail_start = s.ceil_char_boundary(s.len().saturating_sub(max / 4));
    format!("{} {}", &s[..head_end], &s[tail_start..])
}

pub fn extract_text(content: &Value) -> String {
    match content {
        Value::String(s) => s.clone(),
        Value::Array(arr) => {
            let mut parts = Vec::new();
            for block in arr {
                let btype = block.get("type").and_then(Value::as_str).unwrap_or("");
                match btype {
                    "text" => {
                        if let Some(t) = block.get("text").and_then(Value::as_str) {
                            parts.push(t.to_string());
                        }
                    }
                    "tool_use" => {
                        let name = block.get("name").and_then(Value::as_str).unwrap_or("");
                        let mut tool_parts = vec![format!("[Tool: {name}]")];
                        if let Some(inp) = block.get("input").and_then(Value::as_object) {
                            for key in &[
                                "file_path",
                                "command",
                                "description",
                                "pattern",
                                "query",
                                "prompt",
                                "path",
                            ] {
                                if let Some(Value::String(val)) = inp.get(*key) {
                                    let truncated: String = val.chars().take(200).collect();
                                    tool_parts.push(truncated);
                                }
                            }
                        }
                        parts.push(tool_parts.join(" "));
                    }
                    "tool_result" => match block.get("content") {
                        Some(Value::String(inner)) => {
                            let truncated = truncate_for_search(inner, 16 * 1024);
                            parts.push(format!("[Tool Result] {truncated}"));
                        }
                        Some(Value::Array(arr)) => {
                            for item in arr {
                                if let Some(t) = item.get("text").and_then(Value::as_str) {
                                    let truncated = truncate_for_search(t, 16 * 1024);
                                    parts.push(format!("[Tool Result] {truncated}"));
                                }
                            }
                        }
                        _ => {}
                    },
                    _ => {}
                }
            }
            parts.join(" ")
        }
        _ => String::new(),
    }
}

#[derive(Default)]
pub struct MessageContext {
    pub tools: Vec<String>,
    pub files: Vec<String>,
    pub errors: Vec<String>,
}

pub fn extract_context(content: &Value) -> MessageContext {
    let mut ctx = MessageContext::default();
    let Some(arr) = content.as_array() else {
        return ctx;
    };
    for block in arr {
        let btype = block.get("type").and_then(Value::as_str).unwrap_or("");
        match btype {
            "tool_use" => {
                if let Some(name) = block.get("name").and_then(Value::as_str)
                    && !name.is_empty()
                {
                    ctx.tools.push(name.to_string());
                }
                if let Some(inp) = block.get("input").and_then(Value::as_object) {
                    for key in &["file_path", "path"] {
                        if let Some(Value::String(val)) = inp.get(*key) {
                            ctx.files.push(val.clone());
                        }
                    }
                }
            }
            "tool_result" => {
                let texts: Vec<String> = match block.get("content") {
                    Some(Value::String(s)) => vec![s.clone()],
                    Some(Value::Array(arr)) => arr
                        .iter()
                        .filter_map(|item| {
                            item.get("text").and_then(Value::as_str).map(String::from)
                        })
                        .collect(),
                    _ => Vec::new(),
                };
                for inner in &texts {
                    for line in inner.lines() {
                        let ll = line.to_lowercase();
                        if ["error", "exception", "traceback", "failed"]
                            .iter()
                            .any(|w| ll.contains(w))
                        {
                            let truncated: String = line.trim().chars().take(200).collect();
                            ctx.errors.push(truncated);
                        }
                    }
                }
            }
            _ => {}
        }
    }
    ctx
}

pub fn clean_prompt(text: &str) -> String {
    let mut result = CLEAN_USER_QUERY_RE.replace_all(text, "").to_string();
    result = CLEAN_END_QUERY_RE.replace_all(&result, "").to_string();
    result = CLEAN_ATTACHED_RE
        .replace_all(&result, "[attached files]")
        .to_string();
    for re in STRIP_TAG_REGEXES.iter() {
        result = re.replace_all(&result, "").to_string();
    }
    result = CLEAN_MULTI_NL_RE.replace_all(&result, "\n\n").to_string();
    result.trim().to_string()
}

pub fn is_noise(text: &str) -> bool {
    if text.len() < 10 {
        return true;
    }
    let lower = text.to_lowercase();
    NOISE_PATTERNS.iter().any(|p| lower.contains(p))
}

pub fn is_warmup_message(text: &str) -> bool {
    let stripped = text.trim().to_lowercase();
    WARMUP_PATTERNS.contains(&stripped.as_str())
}

pub fn is_clear_metadata(text: &str) -> bool {
    let lower = text.to_lowercase();
    CLEAR_META_PATTERNS.iter().any(|p| lower.contains(p))
}

pub fn is_clear_only_conversation(user_messages: &[String]) -> bool {
    if user_messages.is_empty() {
        return true;
    }
    user_messages.iter().all(|m| {
        let lower = m.trim().to_lowercase();
        lower.is_empty() || lower == "/clear" || lower == "clear" || is_clear_metadata(m)
    })
}

fn find_ci_position(haystack: &str, needle: &str) -> Option<usize> {
    let needle_lower: String = needle.to_lowercase();
    let needle_chars: Vec<char> = needle_lower.chars().collect();
    if needle_chars.is_empty() {
        return Some(0);
    }
    for (i, _) in haystack.char_indices() {
        let mut chars = haystack[i..].chars();
        let mut matched = true;
        for &nc in &needle_chars {
            match chars.next() {
                Some(hc) if hc.to_lowercase().next() == Some(nc) => {}
                _ => {
                    matched = false;
                    break;
                }
            }
        }
        if matched {
            return Some(i);
        }
    }
    None
}

pub fn snippet_around_match(text: &str, query: &str, context_chars: usize) -> String {
    let cleaned = clean_prompt(text);
    let query_terms: Vec<&str> = {
        let terms: Vec<&str> = query.split_whitespace().filter(|w| w.len() > 2).collect();
        if terms.is_empty() {
            query.split_whitespace().filter(|w| w.len() >= 2).collect()
        } else {
            terms
        }
    };
    let query_lower: Vec<String> = query_terms.iter().map(|t| t.to_lowercase()).collect();

    let phrase: String = query_lower.join(" ");
    let best_pos = if let Some(pos) = find_ci_position(&cleaned, &phrase) {
        pos
    } else {
        let mut positions = Vec::new();
        for t in &query_lower {
            if let Some(p) = find_ci_position(&cleaned, t) {
                positions.push(p);
            }
        }
        if positions.is_empty() {
            return cleaned.chars().take(context_chars).collect();
        }
        *positions.iter().min().unwrap()
    };

    let start = cleaned.floor_char_boundary(best_pos.saturating_sub(context_chars / 4));
    let end = cleaned.ceil_char_boundary((start + context_chars).min(cleaned.len()));

    let start = if start > 0 {
        let search_end = cleaned.ceil_char_boundary(start.saturating_add(30).min(cleaned.len()));
        cleaned[start..search_end]
            .find(' ')
            .map(|ws| start + ws + 1)
            .unwrap_or(start)
    } else {
        start
    };
    let end = if end < cleaned.len() {
        let search_start = cleaned.floor_char_boundary(end.saturating_sub(30));
        cleaned[search_start..end]
            .rfind(' ')
            .map(|ws| search_start + ws)
            .unwrap_or(end)
    } else {
        end
    };

    let end = end.max(start);
    let snippet = &cleaned[start..end.min(cleaned.len())];
    let prefix = if start > 0 { "..." } else { "" };
    let suffix = if end < cleaned.len() { "..." } else { "" };
    format!("{prefix}{snippet}{suffix}")
}

#[cfg(test)]
mod tests {
    use super::*;
    use serde_json::json;

    #[test]
    fn extract_text_from_string() {
        let v = json!("hello world");
        assert_eq!(extract_text(&v), "hello world");
    }

    #[test]
    fn extract_text_from_array() {
        let v = json!([
            {"type": "text", "text": "hello"},
            {"type": "text", "text": "world"}
        ]);
        assert_eq!(extract_text(&v), "hello world");
    }

    #[test]
    fn extract_text_tool_use() {
        let v = json!([
            {"type": "tool_use", "name": "Read", "input": {"file_path": "/src/main.rs"}}
        ]);
        let text = extract_text(&v);
        assert!(text.contains("Read"));
        assert!(text.contains("/src/main.rs"));
    }

    #[test]
    fn extract_text_tool_result_string() {
        let v = json!([
            {"type": "tool_result", "content": "some output text"}
        ]);
        let text = extract_text(&v);
        assert!(text.contains("some output text"));
    }

    #[test]
    fn extract_text_tool_result_array() {
        let v = json!([
            {"type": "tool_result", "content": [{"text": "result line"}]}
        ]);
        let text = extract_text(&v);
        assert!(text.contains("result line"));
    }

    #[test]
    fn extract_text_null() {
        assert_eq!(extract_text(&json!(null)), "");
    }

    #[test]
    fn extract_text_number() {
        assert_eq!(extract_text(&json!(42)), "");
    }

    #[test]
    fn clean_prompt_strips_user_query_tags() {
        assert_eq!(
            clean_prompt("<user_query> hello world </user_query>"),
            "hello world"
        );
    }

    #[test]
    fn clean_prompt_strips_attached_files() {
        let text = "hello <attached_files>some files here</attached_files> world";
        assert_eq!(clean_prompt(text), "hello [attached files] world");
    }

    #[test]
    fn clean_prompt_collapses_newlines() {
        assert_eq!(clean_prompt("hello\n\n\n\n\nworld"), "hello\n\nworld");
    }

    #[test]
    fn clean_prompt_strips_system_tags() {
        let text = "hello <system_reminder>internal stuff</system_reminder> world";
        assert_eq!(clean_prompt(text), "hello  world");
    }

    #[test]
    fn is_noise_short_text() {
        assert!(is_noise("hi"));
        assert!(is_noise("ok"));
        assert!(is_noise("yes"));
        assert!(is_noise(""));
    }

    #[test]
    fn is_noise_known_patterns() {
        assert!(is_noise(
            "Hello! I'm Claude, ready to help you with anything"
        ));
        assert!(is_noise("You are Claude Code, an AI assistant"));
    }

    #[test]
    fn is_noise_valid_content() {
        assert!(!is_noise("rebuild docker container with new config"));
        assert!(!is_noise("implement the authentication system"));
    }

    #[test]
    fn warmup_messages() {
        assert!(is_warmup_message("warmup"));
        assert!(is_warmup_message("/clear"));
        assert!(is_warmup_message("  /clear  "));
        assert!(!is_warmup_message("hello world"));
        assert!(!is_warmup_message("clear the cache"));
    }

    #[test]
    fn clear_only_conversation_empty() {
        assert!(is_clear_only_conversation(&[]));
    }

    #[test]
    fn clear_only_conversation_clear_messages() {
        assert!(is_clear_only_conversation(&["/clear".to_string()]));
        assert!(is_clear_only_conversation(&[
            "clear".to_string(),
            "/clear".to_string()
        ]));
    }

    #[test]
    fn clear_only_conversation_real_messages() {
        assert!(!is_clear_only_conversation(&[
            "implement feature X".to_string()
        ]));
    }

    #[test]
    fn snippet_contains_query_term() {
        let text = "This is a long piece of text about implementing authentication with OAuth2 tokens and JWT validation for the API endpoint";
        let snippet = snippet_around_match(text, "authentication", 100);
        assert!(snippet.contains("authentication"));
    }

    #[test]
    fn snippet_fallback_on_no_match() {
        let text = "This is a long piece of text about implementing stuff for the project with lots of details";
        let snippet = snippet_around_match(text, "nonexistent_xyz_term", 100);
        assert!(!snippet.is_empty());
    }

    #[test]
    fn snippet_short_text() {
        let snippet = snippet_around_match("short text here", "short", 200);
        assert!(snippet.contains("short"));
    }

    #[test]
    fn snippet_does_not_panic_on_empty() {
        let snippet = snippet_around_match("", "query", 100);
        assert!(snippet.is_empty() || snippet.len() < 100);
    }

    #[test]
    fn extract_context_tool_uses() {
        let v = json!([
            {"type": "tool_use", "name": "Read", "input": {"file_path": "/src/main.rs"}},
            {"type": "tool_use", "name": "Edit", "input": {"file_path": "/src/lib.rs"}}
        ]);
        let ctx = extract_context(&v);
        assert_eq!(ctx.tools, vec!["Read", "Edit"]);
        assert_eq!(ctx.files, vec!["/src/main.rs", "/src/lib.rs"]);
    }

    #[test]
    fn extract_context_errors_from_tool_result() {
        let v = json!([
            {"type": "tool_result", "content": "Error: file not found\nSuccess: done"}
        ]);
        let ctx = extract_context(&v);
        assert_eq!(ctx.errors.len(), 1);
        assert!(ctx.errors[0].contains("Error"));
    }

    #[test]
    fn extract_context_no_tools() {
        let v = json!([{"type": "text", "text": "just text"}]);
        let ctx = extract_context(&v);
        assert!(ctx.tools.is_empty());
        assert!(ctx.files.is_empty());
        assert!(ctx.errors.is_empty());
    }

    #[test]
    fn extract_context_non_array() {
        let v = json!("just a string");
        let ctx = extract_context(&v);
        assert!(ctx.tools.is_empty());
    }

    #[test]
    fn is_clear_metadata_positive() {
        assert!(is_clear_metadata(
            "caveat: the messages below were generated by the user while running local commands"
        ));
        assert!(is_clear_metadata("some text with <command-name> tag"));
    }

    #[test]
    fn is_clear_metadata_negative() {
        assert!(!is_clear_metadata("implement the new feature"));
    }

    #[test]
    fn truncate_for_search_short() {
        assert_eq!(truncate_for_search("short", 100), "short");
    }

    #[test]
    fn truncate_for_search_long() {
        let long = "a".repeat(1000);
        let result = truncate_for_search(&long, 100);
        assert!(result.len() <= 200); // head + tail with space
    }
}
